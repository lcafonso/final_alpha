            <h4>B. Posicionamento em escala fina (no campo)</h4>
            <p>
                Se encontrou alguns recursos correspondentes e tem certeza de que está na área certa ...Parabens! A parte difícil acabou, mas o esforço que se coloca nos próximos passos fará uma grande diferença na qualidade da repetição da fotografia.
            </p>
            <p>
                Quando estiver em campo e tiver encontrado a área geral, é preciso saber agora exatamente onde a câmera do fotógrafo original foi posicionada no espaço. Paciência e observação cuidadosa neste estágio são criticos, então tome o tempo tempo necessario.
                Comece movendo-se para trás e para frente, para os lados para consegir uma correspondencia mais de fiel à posição original, no tamanho e  ângulos entre os objetos que vemos na realidade, com o que se vê na imagem original.
                Anote os recursos fixos bem definidos, como grandes rochas, edifícios, postes de vedação, postes telefônicos, grandes árvores antigas, silhuetas irregulares de montanhas, etc.
                Tente evitar o uso de recursos que possam mudadar de posição ou crescido no período intermediário.
            </p>
            <p>
                [Uma Dica: Especialmente útil são objetos claramente definidos que se sobrepõem uns aos outros sobre o ponto de vista do fotógrafo e são separados uns dos outros por alguma distância (por exemplo, duas grandes rochas em diferentes profundidades na foto).
                Tais objetos são facilmente identificados por ficar na posição ocupada pelo fotógrafo original (seu melhor palpite nesse estágio é bom) e mover a cabeça de um lado para o outro. Os objetos mais úteis são aqueles que se movem mais em relação um ao outro.
                É aconselhável selecionar mais de um par de recursos fixos (quanto mais, melhor) para se alinharem.]
            </p>

