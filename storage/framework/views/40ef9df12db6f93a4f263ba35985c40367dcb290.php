            <h3>1º encontrar a localização da foto desejada</h3>
            <h4>A. Pesquisa em larga escala (usando o Google Earth no seu computador)</h4>
            <p>
                A pesquisa em larga escala deve ser feita antes de se ir para ao local da foto.
                Isso economizará muito tempo e esforço. A realocação da posição de uma foto é muitas vezes complicada porque a maioria das fotos antigas foram tiradas antes do advento dos satélites em órbita da Terra e dos dispositivos GPS.
                Alguma investigação é necessárias para determinar exatamente de onde a fotografia original foi tirada.
                Felizmente, com o auxílio dos nossos colaboradores e conhecimento local, as fotos na nossa base de dados são publicadas com alguma informação a seu respeito.
                Este é um ponto de partida útil para começar sua caça ao tesouro fotográfico!
                Características topográficas proeminentes, como montanhas e vales, cursos de água, corpos de água, vegetação distinta, estruturas feitas pelo homem, como caminhos ou estradas, podem ser pistas úteis para estreitar ainda mais a busca.
                Vale a pena ter presente que as fotografias originais têm maior probabilidade de estarem localizadas ao lado - ou nas proximidades - de rotas de transporte antigas e atuais (por exemplo, linhas ferroviárias, estradas e caminhos antigos, trilhas, etc.).
            </p>
            <p>
                Ao procurar uma foto especifica, algum conhecimento prévio da paisagem é uma grande vantagem, mas muitas vezes há um elemento de sorte em se escolher um recurso de paisagem que você reconhece a partir da imagem antiga.
                O Google Earth pode ser uma ferramenta poderosa para pesquisar "virtualmente" dentro da área prescrita.
                Ao inclinar a visualização do Google Earth para um ângulo oblíquo (semelhante  visão do fotógrafo) e "sobrevoar" as proximidades da area onde a foto foi tirada, às vezes pode-se encontrar recursos proeminentes, como no exemplo a seguir.
                Essa abordagem é mais útil em áreas com terreno montanhoso ou características óbvias de definição.
            </p>

