            </p>
            <h3>3º Trabalho complementar</h3>
            <ol>
                <li>
                    <strong>Dados completos do local:</strong> Anote os dados de localozação de cada fotografia repetida, coordenadas de GPS (de preferência graus decimais) são muito úteis para quem deseja encontrar a localização da foto original e repetir no futuro, por isso, se você tiver um dispositivo GPS, registre esses detalhes.
                    <p style="font-style: italic; font-size: 90%">
                        <strong>Observação:</strong> se não tiver um GPS, é possível anotar a localização de onde a foto repetida foi tirada usando o Google Maps.
                    </p>
                </li>
                <li>
                    <strong>Anote a altura da câmera (cm):</strong>
                    Essa medida deve ser registada como a distância do solo até o meio da lente da câmera (uma distância aproximada será suficiente se nenhuma fita métrica estiver disponível).
                </li>
                <li>
                    <strong>Marque o local:</strong>
                    se o local permitir, construa um pequeno monte de pedras marcando o local exato em que estava para tirar a foto.
                    Isso funcionará como uma referência precisa à sua localização.
                </li>

            </ol>
