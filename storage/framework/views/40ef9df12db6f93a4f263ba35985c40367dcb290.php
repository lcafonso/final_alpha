            <h3>2º Repetindo a fotografia</h3>
            <p>
                Após o tempo para combinar a posição relativa de vários recursos, finalmente encontramos a mesma posição no espaço que o fotógrafo original.
                Ótimo - agora estamos prontos para repetir a fotografia!
            </p>
            <ol>
                <li>
                    <strong>Tomando a imagem:</strong> Prepare a câmera, estabilize-se o máximo possível, aumente ou diminua o zoom para que sua extensão de visão seja um pouco maior que a da fotografia original… tire a foto!
                </li>
                <li>
                    <strong>Verifique:</strong> verifique se não cortou um lado acidentalmente e se está satisfeito com a foto; é mais fácil fotografar novamente agora do que ter que voltar depois. Pode valer a pena tirar uma segunda foto como back-up, apenas para o caso de algum problema.
                </li>

            </ol>
            <p style="font-style: italic; font-size: 90%">
                [Dica: Se tiver um equipamento adicional, como um tripé e uma câmera Digital Single Lens Reflex, poderá obter um nível muito mais alto de precisão e qualidade fotográfica.
                Se é um fotógrafo mais experiente e está interessado em mais detalhes técnicos, leia as instruções abaixo.]
            </p>
            <p style="font-style: italic; font-size: 90%">
                [Dica: Às vezes, a visão original foi parcialmente ou completamente obscurecida por um objeto árvore, arbusto, variado do que cresceu, caiu, foi construído na frente da posição original da câmera, ou outro obstaculo que não permite acesso a o ponto exato,
                não deixe que isso te impeça de tentar a repetição da fotografia.
                Configure um novo local de foto ao lado da obstrução que permita uma visão desobstruída da cena anterior.]
            </p>
            <p>
                <strong>Nota:</strong> Também pode tirar fotografias adicionais (de grande angular, fotos panorâmicas, fotos de tripé) na foto de repetição principal.
