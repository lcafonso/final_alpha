    <h4>Configurações ideais da câmera:</h4>
    <p>
        <strong>ISO: 100 </strong>- a maior quantidade de detalhes e a menor quantidade de granulação ou "ruído" na imagem digital.
    </p>
    <p>
        <strong>Prioridade de Abertura (AV): f8 </strong> - a maior clareza nas lentes modernas é encontrada nas aberturas de faixa intermediária.
        O uso de um tripé permite velocidades mais lentas do obturador em condições de pouca luz.
    </p>
    <h4>Procedimento</h4>
    <p>
        Supondo que tenha seguido as etapas iniciais e nao encontrou o ponto onde posicionar sua câmera no local de modo a corresponder ao campo de visão do fotógrafo original verifique o seguinte:.
    </p>
    <ol>
        <li>
            <strong>Configuração do tripé:</strong>
            posicione o tripé sobre o local escolhido e monte sua câmera na parte superior.
            Ajuste as pernas do tripé de modo que a placa de montagem da câmera fique o mais nivelada possível (para que possa ficar no mesmo plano ao tirar fotos panorâmicas para os lados).
            Algum reajuste da posição da câmera será necessário para que esteja no lugar certo.
            Gaste o tempo que for necessario aqui, como precisão faz uma grande diferença para a qualidade da repetição.
        </li>
        <li>
            <strong>Tirar a foto</strong>: Agora, aumente o zoom para a extensão aproximada da imagem original e verifique se as bordas da sua imagem correspondem às da foto original.
            Quando estiver satisfeito que a posição relativa, o tamanho e o ângulo entre os recursos no original e na repetição e as bordas do campo de visão estejam bem alinhadas, prossiga e repita a FOTOGRAFIA.
        </li>

        <li>
            <strong>Fotos adicionais:</strong>
            como reservou um tempo para posicionar seu tripé no exacto local da fotografia original,
            considere tirar algumas fotos extras que ajudarão a contextualizar a imagem original e fornecer uma visão mais completa da paisagem:
        <ul>
            <li>
                <strong>Visão de grande angular:</strong> reduza o zoom em toda a extensão da sua lente e faça um disparo de ângulo amplo
            </li>
            <li>
                <strong>Vista esquerda e direita:</strong> Pan 30 ° para a esquerda e para a direita para tirar fotos com ângulos largos em ambos os lados da foto (panorâmica de 15 ° se a foto estiver no formato retrato)
            </li>
            <li>
                <strong>Localização do tripé:</strong> por último, desmontar cuidadosamente a câmera da cabeça do tripé, dar alguns passos para trás e tirar uma foto do local do tripé

            </li>
        </ul>
        </li>
    </ol>

